void rv::putc(char c) {
    * reinterpret_cast<volatile char *>(0x10000000) = c;
    //sbi::ecall(sbi::EID_CONSOLE, sbi::FID_PUTCHAR, c);
}

void rv::puts(const char * str, size_t n) {
    if (n == 0) n = SIZE_MAX;
    while (* str != '\0' && n-- != 0) {
        putc(* (str++));
    }
}
